# Operadores aritiméticos

"""
    soma: +
    subtração: -
    divisão: /
    divisão inteira: //
    resto da divisão: %
    potencia: **
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(numero1 % numero2)
print(numero1 ** numero2)

# Operadores boleanos 

"""
    maior que: >
    maior ou igual a que: >=
    menor que: <
    menor ou igual a que: <=
    igual a que: ==
    diferente de que: !=
"""

a1 = 10
b1 = 20
a2 = 1.75
b2 = 1.65
b3 = b2

print(a1 > b1)
print(a1 < b1)
print("Python" == "python")
print("banana" != "abacaxi")
print(a2 >= b2)
print(a2 > b3)